# dynamic way (parameterized function)
def add(a,b): #passing variables as parameters
    print(a+b)

def sub(a,b):
    print(a-b)

def mul(a,b):
    print(a*b)

def div(a,b):
    print(a/b)

add(10,30) #passing the values(arguments) at teh time of calling the functions.
mul(100,5)
mul(4,7) #same function calling multiple times ,